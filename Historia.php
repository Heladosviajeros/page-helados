<div class="borde">Helados viajeros inicio sus operaciones en el año 2020 en un pequeño local. La heladería nace gracias a la visión de 6 amigos quienes querían brindar a las personas un helado de calidad y excelencia con una variedad innovadora de sabores de helados. Entonces crearon helados viajeros, al principio pensaban en producir helados y paletas de diferentes países, pero les resultaba muy difícil por algunas frutas e ingredientes, posteriormente decidieron que venderían helados artesanales con frutas 100% salvadoreñas, crearon 4 sabores principales: coco, tamarindo, mango, arrayan. A través del tiempo han ido innovando y desarrollando nuevos sabores, satisfaciendo los deseos y necesidades de cada persona como creando helados para personas diabéticas, pero sin perder la calidad de nuestros helados.</div>
<br><br><hr>
<h1>¿Quiénes somos?</h1><br>
<div class="borde">
<h3>Misión</h3>
<p>Ofrecer helados artesanales de alta calidad y de precio justo para las familias salvadoreñas en una amplia variedad de sabores, con nuestros productos buscamos llevar a nuestros clientes a través de un viaje por la cultura salvadoreña con productos 100% naturales y de calidad. Siendo una de las heladerías más competitivas a nivel nacional.</p>
<br></div><br>
<div class="borde">
<h3> Visión</h3>
<p>Ser reconocidos por la calidad de nuestros helados artesanales que ofrecemos, buscando el desarrollo de nuestros productos e innovación de sabores 100% naturales y salvadoreños para satisfacer las necesidades y deseos de nuestros clientes. Al mismo tiempo crecer a través del desarrollo del mercado para convertirnos en la heladería más grande de la región, manteniendo la excelencia y calidad de nuestros productos. </p>
